package edu.wcu.cs.cs363.team3.project03.gui.color;

import java.awt.Color;

/**
 * Interface for observers that are interested in changes to the
 * selected color in ColorSelectionPanel.
 *
 * @author Andy Dalton
 * @author Chris Blades
 * @author Don Coffin
 * @author Benny Reese
 *
 * @version 9/4/2010
 */
public interface ColorChangeListener {
   /**
    * Notify observer that the currently selected color in ColorSelectionPanel
    * has changed.
    *
    * @param color the color currently selected in ColorSelectedPanel
    */
   public void colorChange(Color color);
}
